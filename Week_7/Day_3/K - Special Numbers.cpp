#include "bits/stdc++.h"
using namespace std;

const int mod = 1e9 + 7;

void solve() {
	long long n, k;
	cin >> n >> k;
	
	long long p = 1, ans = 0;
	while (k) {
		ans = (ans + (k & 1) * p) % mod;
		p = p * n % mod;
		k = (k >> 1);
	}
	cout << ans << endl;
}

int main() {
	ios_base::sync_with_stdio(0);
	cin.tie(0);
#ifndef ONLINE_JUDGE
	freopen("input.txt", "r", stdin);
	freopen("output.txt", "w", stdout);
#endif
	int t = 1;
	cin >> t;
	while (t--) solve();
}
